#include <iostream>
#include <cmath>

using namespace std;

int main() {

    cout<<"Print power of pow(2,5): " << pow(2,5) << endl;
    cout <<"Print square of sqrt(36): " << sqrt(36) << endl;
    cout <<"Print round(4.5) value: " << round(4.5) << endl;
    cout <<"Print whole number ceil(4.1): " << ceil(4.1) << endl;
    cout <<"Print floor(4.3): " << floor(4.3) << endl;
    cout <<"Print the max number fmax(3,10): " << fmax(3,10) << endl;
    cout <<"Print the min number fmin(4,1): " << fmin(4,1) << endl;

    return 0;
}
